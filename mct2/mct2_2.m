A=[0 1;-6 -5];
B=[0;1];
C=[1 0];
D=[0];
t=0:0.01:10;
u=3*sin(5*t);x0=[1;-1];
%建立系统状态空间矩阵,设定系统输入和初值。
sys=ss(A,B,C,D);
[y,t,x]=lsim(sys,u,t,x0);
%计算系统在初始条件下的状态响应和输出响应
figure(1);uitable('data',[x(:,1),x(:,2),y],'ColumnName',['状态响应';'状态响应';'输出响应']);
%列表表示初始状态作用下状态响应和输出响应的数值解
figure(2);plot(t,x(:,1),t,x(:,2),t,y);
legend('x1状态响应','x2状态响应','系统输出曲线');
figure(3);plot(t,x(:,1),t,x(:,2));
legend('x1状态响应','x2状态响应');
%绘制系统的状态响应曲线和系统输出曲线
figure(4);plot(x(:,1),x(:,2));